clear all
%Метод Томаса, или метод прогонки
n=5;
b=[0 -5 7 0 2]';
c=[-10 -21 12 8 10]';
d=[-9 -8 2 2 0]';
r=[7 29 31 56 -24]';

B(1)=-d(1)/c(1);
L(1)=r(1)/c(1);

for i=2:1:n
  B(i)=-d(i)/(b(i)*B(i-1)+c(i));
  L(i)=(r(i)-b(i)*L(i-1))/(b(i)*B(i-1)+c(i));
end;

x(n)=L(n);

for i=(n-1):(-1):1
  x(i)=B(i)*x(i+1)+L(i)
end