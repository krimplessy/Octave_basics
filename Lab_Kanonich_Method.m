% Каноничный метод интерполяции
clear all
n = 4
x = [1 3 5 7]';
y = [2 10 5 7]';
%Матрица вондермонда
for i = 1:1:n
  for j = 1:1:n
    X(i, j) = x(i)^(j-1);
    endfor
  endfor
  A = X^(-1)*y;
  %Проверка температуры в разные дни недели
  x0 = 2; % на завтра
  sum = 0;
  for i = 1:1:n
    sum = sum + A(i)*x0^(i-1);
  endfor
sum